// Build library: { '1': { 'A': coords, 'B': coords, ... }, '2': { ... }, ... }
export const SHAPE_LIBRARY = {};
Object.entries(SHAPE_TABLE).forEach(([key, coords]) => {
  const shape = key.slice(0, -1);
  const config = key.slice(-1);
  if (!SHAPE_LIBRARY[shape]) SHAPE_LIBRARY[shape] = {};
  SHAPE_LIBRARY[shape][config] = coords.map(c => [...c]);
});

export const SHAPES = Object.keys(SHAPE_LIBRARY).sort((a, b) =>
  a.localeCompare(b, undefined, { numeric: true }));

export function getConfigs(shape) {
  return Object.keys(SHAPE_LIBRARY[shape] || {}).sort();
}

export function getCoords(shape, config) {
  return SHAPE_LIBRARY[shape]?.[config] || [];
}
